<script setup>
import { storeToRefs } from "pinia";
import { computed, onMounted, ref, watch } from "vue";
import { useRoute, useRouter } from "vue-router";
import NavbarVue from "../components/Navbar.vue";
import MealItem from "../components/MealItem.vue";
import { useRestaurantStore } from "../stores/restaurant";
import { useMealStore } from "../stores/meal";
import NoDataPage from "../components/NoDataPage.vue";
import Pagination from "../components/Pagination.vue";

const router = useRouter();
const route = useRoute();
const restaurantStore = useRestaurantStore();
const mealStore = useMealStore();

const name = ref("kaung");

const { restaurant } = storeToRefs(restaurantStore);
const { meals, loading } = storeToRefs(mealStore);

const chooseType = ref([
  { id: 1, name: "low_to_high" },
  { id: 2, name: "high_to_low" },
]);
const order_by_price = ref("");

const restaurant_id = ref("");
const restaurant_name = ref("");

// const goRoom = () => {
//   router.push({
//     name: "home",
//   });
// };

const createPage = () => {
  router.push({
    name: "meal-create",
  });
};

const goBack = () => {
  router.go(-1);
};

const start_date = ref("");
const end_date = ref("");
const periodAjj = ref("");

const searchFunction = async () => {
  if (start_date.value && end_date.value) {
    periodAjj.value = `${start_date.value} , ${end_date.value}`;
  }
};

const clear = () => {
  restaurant_id.value = "";
  restaurant_name.value = "";
  order_by_price.value = "";
  start_date.value = "";
  end_date.value = "";
  periodAjj.value = "";
};
const watchSystem = computed(() => {
  const result = {};

  if (restaurant_id.value != "" && restaurant_id.value != undefined) {
    result.restaurant_id = restaurant_id.value;
  }
  if (order_by_price.value != "" && order_by_price.value != undefined) {
    result.order_by_price = order_by_price.value;
  }
  if (periodAjj.value != "" && periodAjj.value != undefined) {
    result.period = periodAjj.value;
  }
  return result;
});

const changePage = async (url) => {
  console.log(url);
  await mealStore.getChangePage(url, watchSystem.value);
};

const changes = async (message) => {
  if ((message = "Room Deleted")) {
    restaurant_id.value = "";
  }
};

onMounted(async () => {
  restaurant_id.value = route.params.id;
  restaurant_name.value = route.params.name;
  await restaurantStore.getSimpleListAction();
  // await mealStore.getListAction();
  // console.log(route.params.id, restaurants.value, "this is params id");
});

watch(restaurant_id, async (newValue) => {
  await mealStore.getListAction(watchSystem.value);
  console.log(restaurant_id.value);
});
watch(order_by_price, async (newValue) => {
  await mealStore.getListAction(watchSystem.value);
  console.log(order_by_price.value);
});
watch(periodAjj, async (newValue) => {
  await mealStore.getListAction(watchSystem.value);
  console.log(periodAjj.value);
});
</script>

<template>
  <div class="bg-white">
    <NavbarVue />
    <div class="py-5 px-4 space-y-4">
      <div class="relative">
        <div
          class="flex justify-start text-sm items-center gap-2 text-main absolute top-1"
          @click="goBack"
        >
          <svg
            xmlns="http://www.w3.org/2000/svg"
            fill="none"
            viewBox="0 0 24 24"
            stroke-width="1.5"
            stroke="currentColor"
            class="w-6 h-6"
          >
            <path
              stroke-linecap="round"
              stroke-linejoin="round"
              d="M15.75 19.5L8.25 12l7.5-7.5"
            />
          </svg>
          Back
        </div>

        <p class="text-main text-lg font-semibold w-full text-center">Meals</p>
      </div>
      <div
        class="bg-main/5 py-2 pl-3 pr-2 rounded-xl shadow flex justify-between items-center"
      >
        <div class="mr-2" @click="clear">
          <svg
            xmlns="http://www.w3.org/2000/svg"
            v-if="
              !restaurant_id &&
              !order_by_price &&
              !start_date &&
              !end_date &&
              periodAjj
            "
            fill="none"
            viewBox="0 0 24 24"
            stroke-width="1.5"
            stroke="currentColor"
            class="w-6 h-6 text-main"
          >
            <path
              stroke-linecap="round"
              stroke-linejoin="round"
              d="M21 21l-5.197-5.197m0 0A7.5 7.5 0 105.196 5.196a7.5 7.5 0 0010.607 10.607z"
            />
          </svg>
          <img
            v-if="
              restaurant_id ||
              order_by_price ||
              start_date ||
              end_date ||
              periodAjj
            "
            src="../../public/clear-svgrepo-com (1).svg"
            class="w-6 h-6"
            alt=""
          />
          <!-- <p class="text-main">Search</p> -->
        </div>
        <p
          v-if="restaurant_name"
          class="text-start ml-2 py-1 text-base text-main w-full"
        >
          {{ restaurant_name }}
        </p>
        <v-select
          v-if="!restaurant_name"
          class="style-chooser w-full"
          :options="restaurant?.data"
          v-model="restaurant_id"
          label="name"
          :clearable="false"
          :reduce="(d) => d.id"
          placeholder="Search"
        ></v-select>
      </div>
      <div class="flex py-1.5 mb-5 gap-3 flex-wrap">
        <v-select
          class="style-chooser bg-white rounded-xl border border-main min-w-[150px]"
          :options="chooseType"
          v-model="order_by_price"
          label="name"
          :clearable="false"
          :reduce="(d) => d.name"
          placeholder="Sort by Price"
        ></v-select>

        <button
          class="px-2 py-1.5 bg-[#ff613c] rounded-full text-white"
          @click="searchFunction"
          v-if="start_date && end_date"
        >
          <svg
            xmlns="http://www.w3.org/2000/svg"
            fill="none"
            viewBox="0 0 24 24"
            stroke-width="1.5"
            stroke="currentColor"
            class="w-6 h-6"
          >
            <path
              stroke-linecap="round"
              stroke-linejoin="round"
              d="M21 21l-5.197-5.197m0 0A7.5 7.5 0 105.196 5.196a7.5 7.5 0 0010.607 10.607z"
            />
          </svg>
        </button>
      </div>
      <div
        class="relative flex justify-center items-center py-[50%]"
        v-if="loading"
      >
        <div
          class="absolute animate-spin rounded-full h-24 w-24 border-t-4 border-b-4 border-main"
        ></div>
        <img src="../../public/logo.jpg" class="rounded-full h-16 w-16" />
      </div>
      <div
        class="grid grid-cols-1 md:grid-cols-2 gap-6 pb-5 pt-2"
        v-if="!loading"
      >
        <div
          class="space-y-2"
          v-for="(meal, index) in meals?.data"
          :key="index"
        >
          <MealItem :id="meal.id" :rooms="meal" @change="changes" />
        </div>
      </div>
      <div
        class="grid grid-cols-1 md:grid-cols-2 gap-6 pb-5 pt-2"
        v-if="!loading"
      >
        <div
          class="space-y-2 col-span-1 md:col-span-2"
          v-if="meals?.data.length == 0"
        >
          <NoDataPage />
        </div>
      </div>
      <div>
        <Pagination v-if="!loading" :data="meals" @change-page="changePage" />
      </div>
    </div>
  </div>
</template>

